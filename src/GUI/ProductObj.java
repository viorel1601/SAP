package GUI;

public class ProductObj {
    private String product_code;
    private String product_category;
    private String product_name;
    private String initial_stock;
    private String company_provenience;

    public String getProduct_code() {
        return product_code;
    }

    public void setProduct_code(String product_code) {
        this.product_code = product_code;
    }

    public String getProduct_category() {
        return product_category;
    }

    public void setProduct_category(String product_category) {
        this.product_category = product_category;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getInitial_stock() {
        return initial_stock;
    }

    public void setInitial_stock(String initial_stock) {
        this.initial_stock = initial_stock;
    }

    public String getCompany_provenience() {
        return company_provenience;
    }

    public void setCompany_provenience(String company_provenience) {
        this.company_provenience = company_provenience;
    }
}
